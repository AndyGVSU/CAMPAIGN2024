;candidate_swap() 
;stores current candidate to memory
;loads next candidate to current
_CANDSWAP 
	LDA V_PARTY
	JSR _CANDSAVE
	INC V_PARTY
	LDA V_PARTY
	CMP S_PLAYER
	BNE @WRAP
	LDA #00
	STA V_PARTY
@WRAP JSR _CANDLOAD
	RTS 

;candidate_save(A=party index)
;stores current candidate to memory
_CANDSAVE 
	TAX 
	+__LAB2O V_ALLCND
	LDY #CANDDATA
	JSR _OFFSET
	LDY #00
@LOOP LDA C_SCHEDC,Y
	STA (OFFSET),Y
	INY 
	CPY #CANDDATA
	BNE @LOOP
	RTS 

;candidate_load(A=party index)
;loads saved candidate to current
_CANDLOAD 
	TAX 
	+__LAB2O V_ALLCND
	LDY #CANDDATA
	JSR _OFFSET
	LDY #00
@LOOP LDA (OFFSET),Y
	STA C_SCHEDC,Y
	INY 
	CPY #CANDDATA
	BNE @LOOP
	RTS 

