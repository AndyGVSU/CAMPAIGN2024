;init_cp_issues() 
;issue-based state leans
_INITCPI 
	LDA #00
	STA V_PARTY
	JSR _CANDLOAD
	JSR _CPOFFR
	LDA #01
	STA FSTATE
@LOOP2 
@LOOP1 ;get party offset
	LDA V_PARTY
	CLC 
	ADC #UND_OF1M
	STA FY1
	;get issue bonus
	LDA FSTATE
	STA FARG1
	LDA #00
	STA FRET1


	JSR _CISSUEB
	LDA FRET1
	;LSR ;state lean = issue bonus
	LDY FY1
	STA (CP_ADDR),Y
	LDY V_PARTY
	INY 
	STA (CP_ADDR),Y
	;starting CP = new state lean
	JSR _CANDSWAP
	LDA V_PARTY
	BNE @LOOP1
	INC FSTATE
	JSR _CPOFFI
	LDA FSTATE
	CMP #STATE_C
	BNE @LOOP2
	;reset loaded party
	LDA #00
	STA V_PARTY
	JSR _CANDLOAD

	RTS 

