	RTS 

;draw_visit_bonus() 
_DRWVBON 
	+__COORD P_VBONSR,P_VBONSC



	LDA V_OUTOFM
	BEQ @SKIPO
	LDX #00
@CLRLOOP 
	LDA #$20
	STA V_STRING,X
	INX 
	CPX #$10
	BNE @CLRLOOP
	LDA #$00
	STA V_STRING,X
	+__LAB2XY V_STRING
	JSR _GX_STR
	RTS 
@SKIPO 

	+__LAB2XY T_BONUS
	JSR _GX_STR

	LDA #P_VBON2C
	STA GX_CCOL
	LDA C_VBONUS
	AND #$80
	BNE @MINUS
	LDA #$2B
	STA GX_CIND
	LDA #C_LGREEN
	STA GX_DCOL
	BNE @DRAW
@MINUS 
	LDA #$2D
	STA GX_CIND
	LDA #C_PINK
	STA GX_DCOL
@DRAW 
	JSR _GX_CHAR
	INC GX_CCOL
	LDA C_VBONUS
	AND #$7F
	JSR _DRWPOST

	RTS 

