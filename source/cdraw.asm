;draw_winning_party() 
;region in C_CREG
_DRWWIN 
	LDA #P_WINPTR
	STA GX_CROW

	LDX C_CREG
	LDA D_REGLIM,X
	STA FVAR3
	LDA D_REGLIM-1,X
	STA FSTATE
@LOOP 
	LDA #P_WINPTC
	STA GX_CCOL

	LDX FSTATE
	LDA V_CTRL,X
	TAX 
	JSR _DRWPN1
	LDX FSTATE
	LDA V_TIERES,X
	BEQ @SKIPTIE
	ORA #$30
	STA GX_CIND
	JSR _GX_CHAR
	JMP @SKIPSPC
@SKIPTIE 
	LDA #$20
	STA GX_CIND
	JSR _GX_CHAR
@SKIPSPC 
	JSR _GXINCRW

	INC FSTATE
	LDA FSTATE
	CMP FVAR3
	BNE @LOOP
	RTS 

