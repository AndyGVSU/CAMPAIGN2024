;draw_popular_ec() 
;LOCAL: FVAR2
;_POPSUM called beforehand
;draws total popular vote and EC counts on left
_DRWPOP
	JSR _SUMEC

	LDA #P_TOP
	STA GX_CROW

	LDA #00
	STA FVAR2
@LOOP 
	LDA #P_LEFT
	STA GX_CCOL

	LDX FVAR2
	JSR _DRWPN3
	JSR _GXINCRW

	LDA #P_LEFT
	STA GX_CCOL

	LDY FVAR2
	TYA 
	ASL 
	TAY 
	LDA V_SUMEC,Y
	TAX 
	LDA V_SUMEC+1,Y
	JSR _INT
	JSR _INTWS3
	+__LAB2XY V_STRING
	JSR _GX_STR

	JSR _GXINCRW

	LDA #P_LEFT
	STA GX_CCOL

	LDA S_DRWUND
	BNE @SKIPCHK
	LDA FVAR2
	CMP #UND_PRTY
	BEQ @SKIPUND
@SKIPCHK 
	LDA FVAR2
	ASL 
	TAY 
	LDA V_POPSUM,Y
	STA FARG1
	LDA V_POPSUM+1,Y
	STA FARG2
	LDA V_POPSUM+10
	STA FARG3
	LDA V_POPSUM+11


	STA FARG4
	JSR _PERCENT
	JSR _PERCFMT
	+__LAB2XY V_FPOINT
	JSR _GX_STR
	JSR _GXINCRW
@SKIPUND 
	INC FVAR2
	LDA FVAR2
	CMP S_PLAYER
	BCC @LOOP
	CMP #$05
	BCS @RTS
	LDA #UND_PRTY
	STA FVAR2
	BNE @LOOP
@RTS 
	LDA #P_LEFT
	STA GX_CCOL
	+__LAB2XY T_BLANK3
	JSR _GX_STR ;fill in last row (for erasing)
	RTS 

