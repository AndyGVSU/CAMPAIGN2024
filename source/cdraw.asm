;draw_region_states(A=region) 
;draws upper right state list for given region
;LOCAL: FVAR2,FVAR3
_DRWREGS 
	;draw region at top
	STA FSTATE
	DEC FSTATE
	TAX 
	+__COORD P_REGTPR,P_REGTPC
	LDA #C_LGRAY
	STA GX_DCOL

	JSR _DRWREGN
	;get region bounds
	LDX FSTATE
	LDA D_REGLIM,X
	STA FVAR2
	LDA D_REGLIM+1,X
	STA FVAR3

	LDA #P_REGLSR
	STA GX_CROW
@LOOP LDA #C_DGRAY
	STA GX_DCOL
	LDA #P_REGLSC
	STA GX_CCOL

	LDX FVAR2
	LDA #00
	JSR _INT
	+__LAB2XY V_STRING
	JSR _GX_STR ;state index
	INC GX_CCOL

	LDA #C_LBLUE
	STA GX_DCOL
	LDA FVAR2
	JSR _DRWPOST ;postal code
	INC GX_CCOL
	INC GX_CCOL

	LDX FVAR2
	JSR _DRWSTEC ;ec

	JSR _GXINCRW
	INC FVAR2
	LDA FVAR2
	CMP FVAR3
	BNE @LOOP
	;end of game draw control party + tie
	RTS 

