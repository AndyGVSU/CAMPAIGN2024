	STA FVAR2
	+__LAB2XY T_HEALTH
	JSR _GX_STR

	LDA #P_HEAL2C
	STA GX_CCOL
	LDA FVAR1
	BEQ @SKIPHL
	LDA FVAR2
	STA GX_DCOL
@DLOOP 
	LDA #$2A ;heart
	STA GX_CIND
	JSR _GX_CHAR
	INC GX_CCOL
	DEC FVAR1
	LDA FVAR1
	BNE @DLOOP
@SKIPHL 
