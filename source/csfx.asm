;csfx.asm
;sound effects

_SFXINIT
	LDA #00
	TAX
@LOOP
	STA SFXFRQ,X
	INX
	CPX #$18
	BNE @LOOP
	RTS

_PRIMSFX
	LDA #00
	STA SFXCTRL
	RTS

_PLAYSFX
	LDA #$0F
	STA SFXVOL
	LDA #129
	STA SFXCTRL
	RTS
	
_SFXUP
	JSR _PRIMSFX
	LDA #194
	STA SFXFRQ
	LDA #102
	STA SFXFRQ+1
	LDA #$02
	STA SFXATK
	LDA #$00
	STA SFXSUS
	
	JSR _PLAYSFX
	RTS
	
_SFXDOWN
	JSR _PRIMSFX
	LDA #150
	STA SFXFRQ
	LDA #68
	STA SFXFRQ+1
	LDA #$02
	STA SFXATK
	LDA #$00
	STA SFXSUS
	
	JSR _PLAYSFX
	RTS
	
_SFXFIRE
	JSR _PRIMSFX
	LDA #75
	STA SFXFRQ
	LDA #34
	STA SFXFRQ+1
	LDA #$02
	STA SFXATK
	LDA #$00
	STA SFXSUS
	
	JSR _PLAYSFX

	LDA #149
	STA SFXFRQ
	LDA #68
	STA SFXFRQ+1
	LDA #$06
	STA SFXATK
	LDA #$00
	STA SFXSUS
	
	JSR _PLAYSFX
	RTS